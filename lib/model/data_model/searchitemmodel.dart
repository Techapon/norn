
class SearchItem {
  final String docid;
  final String username;
  final String email;
  final int phone;

  final String gender;
  final int age;
  final double weight;
  final double height;


  const SearchItem({
    required this.docid,
    required this.username,
    required this.email,
    required this.phone,
    required this.gender,
    required this.age,
    required this.weight,
    required this.height,
  });
}


